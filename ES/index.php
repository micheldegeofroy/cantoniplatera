<p class="clear">
La Finca de Can Toni Platera es una propiedad única de más de 300 años de antigüedad con vistas al mar. Ubicada en Can Parra Formentera puede alogar hasta 17 personas, incluido el personal, y está compuesta por cinco parcelas (propiedad) que suman 9700 m2, 3 licencias turísticas, 3 independientes Casas con un total de aproximadamente 350 m2 (25% de terrazas incluidas), casa principal, casa de huéspedes, piscina estudio, cuartos de empleados separados, 3 piscinas, la piscina principal (2.8 x 3.4) piscinas secundarias (1.5 x 4, 2 x 2.5), 4 cocinas , 5 baños, 9 habitaciones, Pool Studio, cuartos separados para empleados. Las dependencias incluyen: lavandería, garaje y trasteros y una plataforma de yoga.
<br>
<br>
Can Toni también tiene sus hornos de pan tradicionales, 1000 m2 de viñedo pre Phylloxéra, jardines mediterráneos botánicos, olivos centenarios, algarrobos, buganvilla, árboles frutales de naranja y limón, palmeras de 40 años, cactus exóticos, más de 12000 m de muros de piedra seca.<br>
<br>
Desde un punto de vista técnico, Can toni cuenta con: Wifi a toda la propiedad, electricidad conectada a la red (energía renovable de Soms), así como un generador de respaldo. La ecología y la sostenibilidad siempre han sido un interés y una necesidad antes de la llegada de la civilización moderna, aparte de los sistemas de recolección y almacenamiento de agua (3 cisternas de agua, 200m2 de superficies de recolección de agua de lluvia sin incluir techos). La propiedad también tiene una matriz solar que aunque no directamente conectado a la red se puede utilizar como respaldo y como una forma de reducir el consumo eléctrico, reduciendo así la huella de carbono de la propiedad. La privacidad y la seguridad están intrínsecamente ligadas a la calidad de vida, por lo que la propiedad está bien aislada desde el exterior con paredes altas y multiples cámaras de seguridad.
</p>

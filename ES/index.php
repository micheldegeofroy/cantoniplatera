<p class="clear">
Toni Plate regresó de las Américas, más específicamente de Cuba, un hombre rico y se dice que escondió su tesoro de plata en los terrenos de la propiedad, algunos dicen que incluso en las paredes de la casa principal. Nunca se encontró el tesoro, pero se descubrió una moneda romana en la casa que él construyó para su hermana Rufina, adyacente a la suya. La moneda se remonta al emperador romano Antonino Pío, el sucesor de Adriano. Esto fecha la habitación del área alrededor del año 140 por lo tanto hace 1888 años.
<br>
<br>

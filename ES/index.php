El descubrimiento de la moneda era sorpresa, ya que Can Parra, el área donde se encuentra la Finca, tiene tierras fértiles y es un terreno alto protegido con una vista sin obstruir de La Mola y la Bahía de Mitjorn. La finca está construida en una posición ventajosa que le da una vista dominante de la bahía y en el cercano y pintoresco puerto pesquero "es torrent des s'alga", un lugar perfecto para un puesto romano con línea de vista al fuerte romano en Mitjorn .
<br>
<br>
Hay muy pocas Fincas antiguas de dos pisos en Formentera, una tierra donde casi todo era para sobrevivir, obviamente era un símbolo de prestigio y Toni evidentemente quería alardear de la riqueza que había adquirido.<br>
<br>
El nombre Can Parra significa dominio de los viñedos y la propiedad tiene su propia producción biológica de pre y filoxera, casi extinta, descendiente directo de los vinos egipcios y romanos.<br>
<br>
La Finca ha sido propiedad y ha sido cuidado por la misma familia desde la década de 1960 cuando Formentera era un paraíso perdido, también fue el hogar de la artista Ermine Valty, donde en su estudio produjo un arte fantástico inspirado en Frida Kahlo pero difícil de categorizar.
</p>

